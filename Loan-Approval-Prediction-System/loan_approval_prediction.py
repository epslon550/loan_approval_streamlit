import numpy as np
import pandas as pd

import matplotlib.pyplot as plt
import seaborn as sns

from sklearn.model_selection import train_test_split
from sklearn.preprocessing import LabelEncoder, StandardScaler

from sklearn.ensemble import RandomForestClassifier

from sklearn.metrics import (
    accuracy_score,
    precision_score,
    recall_score,
    f1_score,
    confusion_matrix,
    classification_report
)

import warnings
warnings.filterwarnings("ignore")

np.random.seed(42)

n = 1500

data = pd.DataFrame({
    'ApplicantID': range(1001, 1001+n),
    'Gender': np.random.choice(['Male', 'Female'], n),
    'Age': np.random.randint(21, 60, n),
    'MonthlyIncome': np.random.randint(15000, 150000, n),
    'LoanAmount': np.random.randint(50000, 1500000, n),
    'CreditScore': np.random.randint(300, 900, n),
    'EmploymentStatus': np.random.choice(
        ['Employed', 'Self-Employed', 'Unemployed'],
        n,
        p=[0.6,0.25,0.15]
    ),
    'ExistingLoans': np.random.randint(0,5,n),
    'LoanTerm': np.random.choice([12,24,36,48,60], n),
    'PropertyArea': np.random.choice(
        ['Urban','Semi-Urban','Rural'],
        n
    )
})

approval = []

for i in range(n):

    score = 0

    if data.loc[i,'MonthlyIncome'] > 50000:
        score += 2

    if data.loc[i,'CreditScore'] > 650:
        score += 3

    if data.loc[i,'EmploymentStatus'] != 'Unemployed':
        score += 2

    if data.loc[i,'ExistingLoans'] <= 2:
        score += 1

    if data.loc[i,'LoanAmount'] < data.loc[i,'MonthlyIncome'] * 25:
        score += 2

    approval.append(1 if score >= 6 else 0)

data['LoanApproved'] = approval

data.head()

data.to_csv("loan_dataset.csv", index=False)

print("Dataset Created Successfully")
print(data.shape)

for col in ['Gender','CreditScore']:
    data.loc[data.sample(frac=0.02).index,col] = np.nan

data.isnull().sum()

data.to_csv("loan_dataset.csv", index=False)

"""Data Cleaning"""

data.drop_duplicates(inplace=True)

data['Gender'].fillna(
    data['Gender'].mode()[0],
    inplace=True
)

data['CreditScore'].fillna(
    data['CreditScore'].median(),
    inplace=True
)

print("Missing Values After Cleaning")
print(data.isnull().sum())

"""Basic Information"""

print(data.info())

print("\n")

print(data.describe())

"""Approval Distribution"""

data['LoanApproved'].value_counts()

""" Employment Status vs Approval"""

plt.figure(figsize=(8,5))

sns.countplot(
    x='EmploymentStatus',
    hue='LoanApproved',
    data=data
)

plt.title("Loan Approval by Employment Status")
plt.show()

"""Correlation Heatmap"""

numeric_data = data.select_dtypes(include=np.number)

plt.figure(figsize=(10,7))

sns.heatmap(
    numeric_data.corr(),
    annot=True,
    cmap='coolwarm'
)

plt.title("Correlation Heatmap")
plt.show()

"""Income vs Loan Amount"""

plt.figure(figsize=(8,5))

sns.scatterplot(
    x='MonthlyIncome',
    y='LoanAmount',
    hue='LoanApproved',
    data=data
)

plt.title("Income vs Loan Amount")
plt.show()

"""Pie Chart"""

approved = data['LoanApproved'].value_counts()

plt.figure(figsize=(6,6))

plt.pie(
    approved,
    labels=['Approved','Rejected'],
    autopct='%1.1f%%'
)

plt.title("Loan Approval Distribution")

plt.show()

"""Encode Categorical Features"""

le = LabelEncoder()

data['Gender'] = le.fit_transform(data['Gender'])

data['EmploymentStatus'] = le.fit_transform(
    data['EmploymentStatus']
)

data['PropertyArea'] = le.fit_transform(
    data['PropertyArea']
)

data.head()

"""Feature Selection"""

X = data.drop(
    ['ApplicantID','LoanApproved'],
    axis=1
)

y = data['LoanApproved']

print(X.head())

"""Feature Scaling"""

scaler = StandardScaler()

X_scaled = scaler.fit_transform(X)

"""Train Test Split"""

X_train, X_test, y_train, y_test = train_test_split(
    X_scaled,
    y,
    test_size=0.2,
    random_state=42
)

print(X_train.shape)
print(X_test.shape)

"""Train Model"""

model = RandomForestClassifier(
    n_estimators=200,
    random_state=42
)

model.fit(X_train,y_train)

print("Model Trained Successfully")

"""Predictions"""

y_pred = model.predict(X_test)

"""Evaluation Metrics"""

accuracy = accuracy_score(y_test,y_pred)

precision = precision_score(y_test,y_pred)

recall = recall_score(y_test,y_pred)

f1 = f1_score(y_test,y_pred)

print("Accuracy :",accuracy)

print("Precision :",precision)

print("Recall :",recall)

print("F1 Score :",f1)

"""Classification Report"""

print(classification_report(
    y_test,
    y_pred
))

"""Confusion Matrix"""

cm = confusion_matrix(
    y_test,
    y_pred
)

plt.figure(figsize=(6,5))

sns.heatmap(
    cm,
    annot=True,
    fmt='d',
    cmap='Blues'
)

plt.xlabel("Predicted")

plt.ylabel("Actual")

plt.title("Confusion Matrix")

plt.show()

